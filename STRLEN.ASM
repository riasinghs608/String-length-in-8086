MESS macro MSG
mov ah,09h
lea dx,MSG
int 21h
ENDM
.MODEL SMALL
.DATA
MSG1 DB,0AH,0DH,"ENTER A STRING : $"
MSG2 DB,0AH,0DH,"LENGTH OF THE STRING IS(DIRECT) : $"
STR1 db 30,?,30 dup ('$')
.CODE

mov ax,@data
mov ds,ax

;GET STRING
MESS MSG1

MOV AH,0AH
LEA DX,STR1
INT 21H

;PRINT LENGTH OF STRING (DIRECT)
MESS MSG2

LEA SI,STR1
INC SI

MOV BL,[SI]
ADD BL,30H

MOV DL,BL
MOV AH,02H
INT 21H


MOV AH,4CH
INT 21H
END